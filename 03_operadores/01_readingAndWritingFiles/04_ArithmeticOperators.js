//01 Operadores aritméticos
//Adición (+)
let suma = 5 + 3;
console.log('La suma de 5 y 3 es', suma);

//Resta (-)
let resta = 5 - 3;
console.log('La resta de 5 y 3 es', resta);

//Multiplicación (*)
let multiplicacion = 5 * 3;
console.log('La multiplicación de 5 y 3 es', multiplicacion);

//División (/)
let division = 5 / 3;
console.log('La división de 5 y 3 es', division);

//Modulo (%)
let modulo = 5 % 3;
console.log('El resto de 5 dividido entre 3 es', modulo);

//Incremento
let incremento = 5;
incremento++;
console.log('Incremento de 5 es', incremento);

//Decremento
let decremento = 5;
decremento--;
console.log('Decremento de 5 es', decremento);
